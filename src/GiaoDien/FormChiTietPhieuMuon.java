    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Chitietpm ct_pm = new Chitietpm();
        
        Phieumuon pm = new Phieumuon(Integer.parseInt(txtMaPM.getText()));
        ct_pm.setPhieumuon(pm);

        Object selectedMaSach = cboMaSach.getSelectedItem();
        int maSach = 0;
        if (selectedMaSach != null) {
            maSach = XuLy.layId(selectedMaSach.toString());

            // Tạo và thiết lập khóa chính cho Chitietphieumuon
            ChitietpmPK chitietphieumuonPK = new ChitietpmPK(Integer.parseInt(txtMaPM.getText()), maSach);
            ct_pm.setChitietpmPK(chitietphieumuonPK);
            ct_pm.setSach(new Sach(maSach));
        }
        try {
            ct_pm.setSoLuongMuon(Integer.parseInt(txtSoLuongMuon.getText()));
            ct_pm.setTinhTrangKhiMuon(txtTinhTrangKhiMuon.getText());
        } 
        catch (NumberFormatException e) {
                XuLy.mesLoi(e, this);
        }
        
        
        int soLuongMuon = Integer.parseInt(txtSoLuongMuon.getText());
        
        Sach kho=ketNoi_Sach.findById(maSach);
        
        if (soLuongMuon > kho.getSoLuong()){
            JOptionPane.showMessageDialog(this, "Không đủ sách");
            return;
        }
        kho.setSoLuong(kho.getSoLuong()-soLuongMuon);
        ketNoi_Sach.sua(kho);
        
        ketNoi_CTPM.addSv(ct_pm);
        loadData();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
         
        if (!txtMaPM.getText().isEmpty() && cboMaSach.getSelectedItem() != null) {
            try{
                int soLuongMuon = Integer.parseInt(txtSoLuongMuon.getText());
        
                Sach kho=ketNoi_Sach.findById(XuLy.layId(cboMaSach.getSelectedItem().toString()));

                kho.setSoLuong(kho.getSoLuong()+soLuongMuon);
                ketNoi_Sach.sua(kho);
                ketNoi_CTPM.xoa(new ChitietpmPK(Integer.parseInt(txtMaPM.getText()), 
                        XuLy.layId(cboMaSach.getSelectedItem().toString())));
                clear();
                loadData();
            } catch(Exception e){
               XuLy.mesLoi(e, this);
            }    
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row =jTable1.getSelectedRow();
        if(row>=0){
            txtMaPM.setText(jTable1.getValueAt( row, 0).toString());
            cboMaSach.setSelectedItem(jTable1.getValueAt(row, 1).toString());
            txtSoLuongMuon.setText(jTable1.getValueAt( row, 2).toString());
            txtTinhTrangKhiMuon.setText(jTable1.getValueAt( row, 3).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

