    public FormChiTietPhieuMuon(String maPM) {
        initComponents();
        this.maPhieuMuon = maPM;
        txtMaPM.setText(maPM);
        model = (DefaultTableModel) jTable1.getModel();
        
        for(Sach sach:listsach){
            cboMaSach.addItem(sach.getMaSach()+"-"+sach.getTenSach());
        }
        
        loadData();
        
        Phieumuon pm = ketNoi_PM.findById(Integer.parseInt(maPM));
        if (pm.getTrangThai() == true) {
            btnThem.setEnabled(false);
            btnXoa.setEnabled(false);
        }
    }
    
    void loadData() {
        clear();
        ketNoi_CTPM.loaddataChitet(model, new String[] {"getInforPhieuMuon", 
            "getInforSach", "getSoLuongMuon", "getTinhTrangKhiMuon"}, maPhieuMuon);
    }
    
    void clear() {
        cboMaSach.setSelectedIndex(-1);
        ketNoi_CTPM.clear(new JTextField[] {txtSoLuongMuon, txtTinhTrangKhiMuon}, model);
    } 
    
